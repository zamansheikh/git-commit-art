const makeCommits = (dates) => {
  if (dates.length === 0) {
    return simpleGit().push();
  }
  const DATE = dates[0];
  const data = {
    date: DATE,
    author: "Zaman Sheikh"
  };
  jsonfile.writeFile(FILE_PATH, data, () => {
    simpleGit().add([FILE_PATH]).commit(DATE, {'--date': DATE}, () => {
      makeCommits(dates.slice(1));
    });
  });
};

const getRandomDates = (n) => {
  const base_date = moment().subtract(1, 'year').startOf('week');
  const dates = [];
  for (let i = 0; i < n; i++) {
    const col = random.int(0, 51); // 52 weeks
    const row = random.int(0, 6);  // 7 days
    const date = base_date.clone().add(col, 'weeks').add(row, 'days').format('YYYY-MM-DD');
    dates.push(date);
  }
  return dates;
};

const getTextDates = (text, start_date) => {
  const base_date = moment(start_date).startOf('week');
  const dates = [];
  const letters = text.toUpperCase().split('');
  letters.forEach((letter, index) => {
    const pattern = letterPatterns[letter];
    if (!pattern) {
      console.warn(`No pattern for letter: ${letter}`);
      return;
    }
    for (let row = 0; row < 7; row++) {
      for (let pattern_col = 0; pattern_col < 5; pattern_col++) {
        if (pattern[row][pattern_col] === 1) {
          const col = index * 6 + pattern_col;
          const date = base_date.clone().add(col, 'weeks').add(row, 'days').format('YYYY-MM-DD');
          dates.push(date);
        }
      }
    }
  });
  return dates;
};

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

rl.question("Choose an option: 1. Random commits 2. Custom text commits\n", (option) => {
  if (option === '1') {
    rl.question("Enter the number of commits: ", (n) => {
      const numCommits = parseInt(n);
      if (isNaN(numCommits) || numCommits <= 0) {
        console.log("Please enter a valid number of commits.");
        rl.close();
        return;
      }
      const dates = getRandomDates(numCommits);
      makeCommits(dates);
      rl.close();
    });
  } else if (option === '2') {
    rl.question("Enter the text to display: ", (text) => {
      rl.question("Enter the start date (YYYY-MM-DD): ", (start_date) => {
        if (!moment(start_date, 'YYYY-MM-DD', true).isValid()) {
          console.log("Please enter a valid date in YYYY-MM-DD format.");
          rl.close();
          return;
        }
        const dates = getTextDates(text, start_date);
        if (dates.length === 0) {
          console.log("No valid dates generated. Check your text input.");
          rl.close();
          return;
        }
        makeCommits(dates);
        rl.close();
      });
    });
  } else {
    console.log("Invalid option. Please choose 1 or 2.");
    rl.close();
  }
});